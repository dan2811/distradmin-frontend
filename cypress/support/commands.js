// -- This is a parent command --
Cypress.Commands.add('login', async (email, password) => {
  const loginEndpoint = 'http://localhost:1337/api/auth/local';
  const data = {
    identifier: email,
    password: password,
  };
  // const response = cy.request('POST', loginEndpoint, data);
  const response = await fetch(loginEndpoint, {
    method: 'POST',
    mode: 'cors',
    cache: 'no-cache',
    credentials: 'same-origin',
    headers: {
      'Content-Type': 'application/json',
    },
    redirect: 'follow',
    referrerPolicy: 'no-referrer',
    body: JSON.stringify(data),
  });

  // console.log('LOCAL AUTH RES: ', await response.json());
  const { jwt, gUser } = await response.json();
  localStorage.setItem('token', jwt);
  localStorage.setItem('gUser', gUser);
});
